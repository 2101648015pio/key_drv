#define KEY_DRV_ALL2_H_

/*********************************************************************************************************
 key 消息
 *********************************************************************************************************/
typedef struct {
    UINT32 KEY_num;                                                     /*  key 号,为0时代表所有按键都是
                                                                            弹起状态                     */
    UINT32 KEY_stat;                                                    /*  key 状态                     */
} KEY_MSG;
typedef KEY_MSG *PKEY_MSG;

/*********************************************************************************************************
 模式命令字
 *********************************************************************************************************/
#define CMD_CTL_INT         (100 + 1)                                   /*  中断模式                     */
#define CMD_CTL_POLL        (100 + 2)                                   /*  轮询模式                     */

#endif /* KEY_DRV_ALL2_H_ */
